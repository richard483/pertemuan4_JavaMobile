package Template;

public class Bangunan {
	private String jenisTembok;
	private Integer luas;
	private Double jumlahLantai;
	
	
	public Integer getLuas() {
		return luas;
	}
	public void setLuas(Integer luas) {
		this.luas = luas;
	}
	public String getJenisTembok() {
		return jenisTembok;
	}
	public void setJenisTembok(String jenisTembok) {
		this.jenisTembok = jenisTembok;
	}
	public Double getJumlahLantai() {
		return jumlahLantai;
	}
	public void setJumlahLantai(Double jumlahLantai) {
		this.jumlahLantai = jumlahLantai;
	}
}
